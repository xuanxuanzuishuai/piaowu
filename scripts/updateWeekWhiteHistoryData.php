    /**
     * 获取奖励列表
     * @return mixed
     */
    public function getAwardList(){
        $where = [
            'uuid'  => $this->uuids,
            'status' => ErpUserEventTaskAwardGoldLeafModel::STATUS_GIVE,
            'event_task_id' => $this->eventTaskId,
            'create_time[<>]' => [strtotime($this->startTime), strtotime($this->endTime)],
        ];

        $fields = [
            'id',
            'uuid',
            'award_num',
        ];

        $list = ErpUserEventTaskAwardGoldLeafModel::getRecords($where, $fields);

        $data = [];

        foreach ($list as $value){
            $data[$value['uuid']][] = $value;
        }

        $res = [];
        foreach ($data as $uuid => $item){
            $award_num_count = 0;
            $award_ids = [];
            foreach ($item as $one){
                $award_num_count += $one['award_num'];
                $award_ids[] = $one['id'];
            }
            $tmp = [
                'uuid' => $uuid,
                'award_num_count' => $award_num_count,
                'award_ids'       => $award_ids
            ];
            $tmp['uuid'] = $uuid;

            $res[] = $tmp;
        }

        return $res;
    }

