    public function run(){

        $awardList = $this->getAwardList();

        $n = ceil(count($awardList) / $this->batch_num);
        for($i = 0; $i < $n; $i ++){
            $tmpList = array_slice($awardList, $i * $this->batch_num, $this->batch_num);
            $this->batch_exec($tmpList);
        }

        echo json_encode(['actual'=>$this->uuidStatus, 'should'=>$awardList]);die;
    }

    /**
     * 批量处理
     * @param $awardList
     */
    public function batch_exec($awardList){
        $uuids = array_column($awardList, 'uuid');
        $openIds = $this->getOpenIds($uuids);
        $course_manage_ids = $this->getCourseManageIds($uuids);

        $accountList = $this->getStudentAccount($uuids);

        $accountSucc = array_column($accountList['success'], null, 'uuid');
        $accountNot  = array_flip($accountList['uuid_not_found']);

        foreach ($awardList as $one){

            $this->uuidGrant = [
                'uuid' => $one['uuid'],
                'course_manage_id'  => $course_manage_ids[$one['uuid']]['course_manage_id'],
                'grant_money'       => 0,
                'operator_id'       => 0,
            ];

            if(isset($accountSucc[$one['uuid']])){
                $studentLeaf = $accountSucc[$one['uuid']]['num'] / 100;

                $openId = $openIds[$one['uuid']]['open_id'] ?? null;
                if($studentLeaf > 0){ //uuid存在&金叶子>0
                    if($studentLeaf > $one['award_num_count']){
                        $decLeaf = intval($one['award_num_count']);
                    }else{
                        $decLeaf = $studentLeaf;
                    }

                    $this->decAndSendRedPkg($one, $decLeaf, $openId);

                }else{
                    //金额为0时发送成功状态
                    $this->uuidGrant['reason'] = '发送成功';
                    $this->uuidGrant['status'] = WhiteGrantRecordModel::STATUS_GIVE_NOT_SUCC;
                    $this->uuidGrant['grant_step'] = WhiteGrantRecordModel::GRANT_STEP_5;
                    $this->uuidGrant['award_ids'] = implode(',', $one['award_ids']);
                    $this->uuidGrant['open_id'] = $openId;

                    $this->create($this->uuidGrant);
                    return false;
                }

            }else{
                if(isset($accountNot[$one['uuid']])){
                    $this->uuidStatus['notExists'][] = $one['uuid'];
                }else{
                    $this->uuidStatus['unknown'][] = $one['uuid'];
                }
            }
        }
    }

    /**
     * 扣减并发送金叶子
     * @param $one
     * @param $leaf
     * @param $openId
     */
    public function decAndSendRedPkg($one, $leaf, $openId){

        $data = [
            'uuid' => $one['uuid'],
            'datatype' => ErpStudentAccountModel::DATA_TYPE_LEAF,
            'num'   => $leaf * 100,
            'source_type' => Erp::SOURCE_TYPE_OP_TO_MONEY,
            'operator_uuid' => 0,
            'remark'    => 'OP系统扣减兑换现金',
        ];

        if($this->debug){
            $res = true;
        }else{
            $res = (new Erp())->reduce_account($data);
        }

        if(!$res){ //扣除金叶子成功发红包
            $this->uuidGrant['open_id'] = $openId;
            $this->uuidGrant['grant_money'] = $leaf;
            $this->uuidGrant['reason'] = '金叶子扣减失败';
            $this->uuidGrant['status'] = WhiteGrantRecordModel::STATUS_GIVE_FAIL;
            $this->uuidGrant['grant_step'] = WhiteGrantRecordModel::GRANT_STEP_3;
            $this->uuidGrant['award_ids'] = implode(',', $one['award_ids']);
            $this->create($this->uuidGrant);
            return false;
        }

        if(is_null($openId)){
            $this->uuidGrant['reason'] = 'open_id不存在';
            $this->uuidGrant['status'] = WhiteGrantRecordModel::STATUS_GIVE_FAIL;
            $this->uuidGrant['grant_step'] = WhiteGrantRecordModel::GRANT_STEP_4;
            $this->uuidGrant['award_ids'] = implode(',', $one['award_ids']);
            $this->create($this->uuidGrant);
            return false;
        }

        $appId    = Constants::SMART_APP_ID;
        $busiType = DssUserWeiXinModel::BUSI_TYPE_STUDENT_SERVER;
        $wx = new WeChatPackage($appId,$busiType,WeChatPackage::WEEK_FROM);
        $keyCode = 'REISSUE_PIC_WORD';
        list($actName, $sendName, $wishing) = CashGrantService::getRedPackConfigWord($keyCode);

        $mchBillNo = $this->getMchBillNo([
            'awardIds' => min($one['award_ids']),
            'awardNum' => $leaf
        ]);

        if($this->debug){
            $resultData = true;
        }else{
            $resultData = $wx->sendPackage($mchBillNo, $actName, $sendName, $openId, $leaf, $wishing, 'redPack');
        }

        if($this->debug){
            $this->uuidStatus['sendInfo'][] = [$one['uuid'] => $leaf];
        }

        $this->uuidGrant['open_id'] = $openId;
        $this->uuidGrant['grant_money'] = $leaf;
        $this->uuidGrant['grant_step'] = WhiteGrantRecordModel::GRANT_STEP_5;
        $this->uuidGrant['award_ids'] = implode(',', $one['award_ids']);

        if(empty($resultData) || trim($resultData['result_code']) != WeChatAwardCashDealModel::RESULT_SUCCESS_CODE){
            $this->uuidGrant['bill_no'] = $mchBillNo;
            $this->uuidGrant['reason'] = '发送红包失败';
            $this->uuidGrant['status'] = WhiteGrantRecordModel::STATUS_GIVE_FAIL;
            $this->uuidGrant['result_code'] = $resultData['err_code'] ?? '';
            $this->create($this->uuidGrant);
        }else{
            $this->uuidGrant['reason'] = '发放成功';
            $this->uuidGrant['bill_no'] = $mchBillNo;
            $this->uuidGrant['status'] = WhiteGrantRecordModel::STATUS_GIVE;
            $this->uuidGrant['result_code'] = $resultData['err_code'] ?? '';
            $this->create($this->uuidGrant);
        }
    }

